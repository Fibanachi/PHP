Если переменная $a не равна нулю,
то выведите 'Верно', иначе выведите
'Неверно'. Проверьте работу скрипта при $a, равном 1, 0, -3.
<?php
$a = 1;
if ($a != 0){
    echo "Wright \n";
} else {
    echo "Wrong \n";
}
$a = 0;
if ($a != 0){
    echo "Wright \n";
} else {
    echo "Wrong \n";
}
$a = -3;
if ($a != 0){
    echo "Wright \n";
} else {
    echo "Wrong \n";
}